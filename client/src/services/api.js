import axios from 'axios';

// ĞŸÑ€ÑĞ¼Ğ¾Ğ¹ URL Ğº Ğ±ÑĞºĞµĞ½Ğ´Ñƒ
const API_BASE_URL = 'http://localhost:5000/api';

const api = axios.create({
  baseURL: API_BASE_URL,
  timeout: 10000,
  headers: {
    'Content-Type': 'application/json',
  },
});

// Ğ”Ğ¾Ğ±Ğ°Ğ²Ğ»ÑĞµĞ¼ Ğ»Ğ¾Ğ³Ğ¸Ñ€Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ Ğ·Ğ°Ğ¿Ñ€Ğ¾ÑĞ¾Ğ²
api.interceptors.request.use(
  (config) => {
    console.log(`ğŸ”„ API Request: ${config.method?.toUpperCase()} ${config.url}`);
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => {
    console.error('âŒ API Request Error:', error);
    return Promise.reject(error);
  }
);

// Ğ›Ğ¾Ğ³Ğ¸Ñ€Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ Ğ¾Ñ‚Ğ²ĞµÑ‚Ğ¾Ğ²
api.interceptors.response.use(
  (response) => {
    console.log(`âœ… API Response: ${response.status} ${response.config.url}`);
    return response;
  },
  (error) => {
    console.error('âŒ API Response Error:', error.message);
    
    if (error.response) {
      console.error('ğŸ“Š Response data:', error.response.data);
      console.error('ğŸ”§ Response status:', error.response.status);
    } else if (error.request) {
      console.error('ğŸŒ No response received:', error.request);
    }
    
    if (error.response?.status === 401) {
      localStorage.removeItem('token');
      localStorage.removeItem('user');
      window.location.href = '/login';
    }
    return Promise.reject(error);
  }
);

export default api;